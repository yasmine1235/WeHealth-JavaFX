package entities;

import java.util.List;

/**
 *
 * @author yasmi
 */
public class Article_like {
     private int id;
     private Article article;
     private User user ;

    public Article_like() {
    }

    public Article_like(int id, Article article, User user) {
        this.id = id;
        this.article = article;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Article getArticle() {
        return article;
    }

    public void setArticle(Article article) {
        this.article = article;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }
     
    
}
